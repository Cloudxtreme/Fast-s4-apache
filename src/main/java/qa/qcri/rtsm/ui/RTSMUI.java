    private void ac_absTS_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ac_absTS_btnActionPerformed
        visitsAnalysis = new VisitsAnalysis();
        visitsAnalysis.getTimeSeriesKeyspace();
        tweetsAnalysis = new TweetsAnalysis();
        tweetsAnalysis.getTweetsKeyspace();
        fbAnalysis = new FacebookShareAnalysis();
        fbAnalysis.getTimeSeriesKeyspace();
        acTSList.clear();
        
        String key= ac_key_txtb.getText().trim();
        
        int lower_bound = Integer.parseInt(ac_shares_lowerb_txtb.getText().trim());
        TimeSeriesSorted visitTS = visitsAnalysis.getTimeSeriesColumnValues(key, TimeSeriesIntervals.KEY_ONE_MINUTE, lower_bound);
        visitTS = visitsAnalysis.getAveNormalizedSeries(visitTS);
        visitTS.setAlias("Visits");
        visitTS.setChartSeries(StaticVars.SERIES_TYPE_NORMALIZED);
        
        TimeSeriesSorted FBTS = fbAnalysis.getTimeSeriesColumnValues(key, TimeSeriesIntervals.KEY_ONE_MINUTE_FB, lower_bound);
        FBTS = fbAnalysis.constructRelativeSeries2(FBTS, 1, StaticVars.TIME_FORMAT_MIN);
        FBTS.setAlias("FacebookShares");
        FBTS.setChartSeries(StaticVars.SERIES_TYPE_RELATIVE);

        TimeSeriesSorted tweetsTS = tweetsAnalysis.constructAccumulativeTweetsTS(key);
        tweetsTS = tweetsAnalysis.constructRelativeSeries(tweetsTS, 1, StaticVars.TIME_FORMAT_MIN);
        tweetsTS.setAlias("Tweets");
        tweetsTS.setChartSeries(StaticVars.SERIES_TYPE_RELATIVE);
        
        acTSList.add(visitTS);
        acTSList.add(FBTS);
        acTSList.add(tweetsTS);
        
    }//GEN-LAST:event_ac_absTS_btnActionPerformed

