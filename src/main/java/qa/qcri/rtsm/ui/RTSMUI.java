    private void v_loadTS_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_v_loadTS_btnActionPerformed

        
        Integer keysRange = Integer.parseInt(keysTxtb.getText());
        Integer visitLimit = Integer.parseInt(visitTxtb.getText());
        visitsAnalysis = new VisitsAnalysis();
        visitsAnalysis.getTimeSeriesKeyspace();
        sortedTSVisits = new ArrayList<TimeSeriesSorted>();
        List<String> keysList;
        String specifiedKey = specificKeytxtb.getText().trim();
        if (specifiedKey.isEmpty()) {
            if (all_keys_chk.isSelected()) {
                keysList = visitsAnalysis.getTimeSeriesKeyspace().getAllArticles(StaticVars.ALJAZEERA_DOMAIN);
            } else {
                keysList = visitsAnalysis.getTimeSeriesKeyspace().getNKeys(keysRange, StaticVars.ALJAZEERA_DOMAIN);
            }
        } else {
            visitsAnalysis.getTimeSeriesKeyspace();
            keysList = new ArrayList<String>();
            keysList.add(specifiedKey);
        }
        
        for (String key: keysList){
            v_ts = new TimeSeriesSorted();
            v_ts = visitsAnalysis.getTimeSeriesColumnValues(key, timeInterval_dbox.getSelectedItem().toString(), visitLimit);
            sortedTSVisits.add(v_ts);
        }
        
        dataStatuslb.setText("LOADED");
    }//GEN-LAST:event_v_loadTS_btnActionPerformed

